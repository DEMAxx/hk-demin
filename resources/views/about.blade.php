            <a id="faq1"/>
            <p style="font-family: 'MyriadProBold';color: #676767;">Вопрос: Зачем в настройках указывать данные о весе или габаритах заказа?</p>
            <p><span class="impt">Ответ: </span> Эти данные необходимы для того, чтобы можно было рассчитать стоимсоть доставки, если в каком-то из товаров у вас не указаны габариты или вес.
            В этом случае в запросе на расчет стоимости доставки будут переданы значения по умолчанию, которые вы указали в настрокай приложения.
                <div class="text-success col-md-12">
                    Не забывайте, что в таком случае стоимость заказа будет рассчитана приблизительно.
                    Ведь при передаче заказов на склад - они будут измерены заново, и после этого может измениться стоимость доставки как в сторону увеличения, так и в сторону уменьшения.
                </div>
            </p>
