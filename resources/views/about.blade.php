            <a id="faq2"/>
            <p style="font-family: 'MyriadProBold'; color: #676767;">Вопрос: Как производится измерение габаритов заказа для расчета стоимости доставки?</p>
            <p><span class="impt">Ответ: </span> Ниже описан алгоритм расчета весогабаритных характеристик заказа.</p>

            <p>Описание алгоритма расчета веса и габаритов характеристик товаров, для передачи на расчет стоимости доставки.</p>

            <p>Из заказа мы получаем список товаров и параметры настроек клиента (габариты, вес - для всего заказа или товара).
            <ol>
                <li>Сортируем в порядке возрастания параметры высота, ширина, длина.</li>
                <li>Далее для каждого товара выполняем действия:
                    <ul>
                        <li>считываем габариты</li>
                        <li>сортируем габариты по возрастанию</li>
                        <li>если, количество товаров более 1, то наименьший габарит товара умножаем на количество товаров и полученные значения опять сортируем</li>
                    </ul>
                </li>
                <li>Затем сравниваем соответствующие значения (меньшее с меньшим, среднее со средним, наибольшее с наибольшим) из настроек и из товаров и выбираем из них максимальные.</li>
            </ol>
            </p>
            <p>
                Пример: есть заказ со следующими товарами :<br/>
                1. товар 1 (10 * 12 * 15) 1 шт<br/>
                2. товар 2 (5 * 20 * 10 ) 3 шт<br/>
                3. товар 3 (7 * 3 * 25) 1 шт<br/>
                И в настройках указаны габариты (12, 12, 12)
            </p>
            <p>
                Тогда для сравнения будут использованы следующие значения:<br/>
                (12, 12, 12)<br/>
                (10, 12, 15)<br/>
                (10, 15, 20) ( 15 = 5 * 3 , минимальную сторону увеличиваем на количество товаров и заново сортируем)<br/>
                (3, 7, 25)<br/>
                <br/>
                В итоге заказ будет рассчитан с габаритами 12, 15, 25.
            </p>
